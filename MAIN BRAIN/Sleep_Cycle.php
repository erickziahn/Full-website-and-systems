     <h1>The Sleep Cycle</h1>
        
        <p>
            One sleep cycle comprises of four stages and lasts for about 90-120  minutes. Note that some books list five stages in the sleep cycle. These books consider the first five to ten minutes when you are falling asleep as a stage in the sleep cycle. We think this is more of a transitional phase and not really part of the cycle, especially since this stage of sleep does not repeat itself, while the other four stages do repeat themselves throughout the night. For this reason, we have excluded as part of the cycle.
        </p>
           <h1>The Stages Of Sleep</h1>
        <p>
            <em>Dreams can occur in any of the four stages of sleep, but the most vivid and memorable dreams occur in the last stage of sleep (also commonly referred to as REM sleep).</em>
            
            The sleep cycle repeats itself about an average of four to five times per night, but may repeat as many as seven times. Thus, you can see how a person has several different dreams in one night. However, most people only remember dreams that occur closer toward the morning when they are about  to wake up. But just because you can't remember those dreams do not mean that they never happened. Some people believe that they simply do not dream, when in reality, they just don't remember their dreams.

        </p>
    
    <h2>The Stages Of Sleep</h2>
        <p>
            Once you have an understanding of your dreams, it has the power to unify the body, mind, and spirit. It provides you with insight into your own self and a means for further self-exploration. You will gain a better understanding and discovery of your true self.  So stay awhile -- explore, discover, have fun, make friends, and find out what's in YOUR dream?!
        </p>
        <p>The stages in the sleep cycle are organized by the changes in specific brain activity.
        </p>
        <p>
        <b>Stage 1: </b>You are entering into light sleep. This stage is characterized by Non-rapid eye movements (NREM), muscle relaxation, lowered body temperature and slowed heart rate. The body is preparing to enter into deep sleep.
        </p>
        <p>
        <b>Stage 2:</b>Also characterized by NREM, this stage is characterized by a further drop in body temperature and relaxation of the muscles. The body's immune system goes to work on repairing the day's damage. The endocrine glands secrete growth hormones, while blood is sent to the muscles to be reconditioned. In this stage, you are completely asleep.</p>
        <p>
        <b>Stage 3: </b>Still in the NREM stage, this is an even deeper sleep. Your metabolic levels are extremely slow.</p>
        <p>
        <b>Stage 4: </b>In this stage of sleep, your eyes move back and forth erratically as if watching something from underneath your eyelids. Referred to as REM sleep or delta sleep, this stage occurs at about 90-100 minutes after the onset of sleep. Your blood pressure rises, heart rate speeds up, respiration becomes erratic and brain activity increases. Your involuntary muscles also become paralyzed or immobilized. This stage is the most restorative part of sleep. Your mind is being revitalized and emotions is being fine tuned. The majority of your dreaming occurs in this stage. If you are awakened during this stage of sleep, you are more likely to remember your dreams.
        </p>
        <p>These stages repeat themselves throughout the night as you sleep. As the cycle repeats, you will spend less time in stages 1 to 3 and more time dreaming in stage 4. In other words, it will be quicker and quicker for you to get to stage 4 each time the cycle repeats.</p>
