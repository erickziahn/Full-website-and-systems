     <h1><u><b>CHASE DREAM</b></u></h1>
        <h2>"I'm Being Chased!"</h2>
        
        <p>
            Being chased in your dream is one of several common dream theme stemming from feelings of anxiety in your waking life. In such dreams, you could be running from an attacker, an animal, a monster or some unknown figure who wants to hurt or possibly kill you. Fight or flight is an instinctive response to a physical threat in the environment. Thus it is natural to run or hide or try to outwit your pursuer. 
        </p>
        <h2>Being chased signifies avoidance:</h2>
        <p>
            <em>To dream that you are being chased is an indication that you are avoiding some issues in your waking life.</em>
            
              The actions in your dream exemplify how you respond to pressure and cope with fears, stress or various situations. Instead of confronting the situation, you have a tendency to run away and avoid issues that you are uncomfortable with addressing.. 
        </p>
        <h2>Being chased signifies close-mindedness :</h2>
    
        <p>
            If someone is chasing you in your dream, then it may also refer to your close-mindedness. You are refusing to acknowledge a certain viewpoint or idea and don't even want to give a listen to any opinion that is different from yours.
        </p>
        <h2>Being chased signifies running away from yourself ::</h2>
        <p>
            Another way to analyze your chase dream is how the pursuer or attacker may be an aspect of your own Self. Perhaps you are suppressing or rejecting certain feelings or certain characteristics of your Self. Anger, jealousy, fear, and even love can manifest as a threatening figure in your dream. And you could  be projecting these feelings onto the unknown chaser. 
        </p>
    <h2>Being chased signifies fear:</h2>
        
        <p>Running from an attacker in your dream may simply represent your fear of being attacked. Such dreams are more common among women than men, who may feel physically vulnerable in their surroundings. All you have to do is turn on the news to hear stories of violence and sexual assault.
        </p>
         <h2>You are the one chasing:</h2>
        
        <P>If you are the one doing the chasing, then the dream may highlight your drive and ambition to go after something you want, or go after someone you want. On the other hand, it may also mean that you are falling behind in some aspect of your life and are having to catch up to others. 
        </P>
        <h2>Conclusion:</h2>
        <p>In understanding your own dream of being chased, ask yourself who is chasing you so that you can gain a better understanding . What are you running from? Turn around and confront your pursuer and ask them why they are chasing you.
        </p>

        <p>
        Consider the distance or gap between you and your pursuer. This indicates your closeness to the issue. If the pursuer is gaining on you, then it suggests that the problem is not going to go away. The problem will surround you, until you confront and address it. However, if you are able to widen the gap between your pursuer, then you are able to successfully distance yourself from the problem. In essence, the problem is fading away.
        </p>
